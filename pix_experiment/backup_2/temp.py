import numpy as np

# 1. 분자(Numerator) 리스트 생성
# 0.0부터 255.0까지 0.01 간격으로
start_num = 0.0
max_num = 255.0
step_num = 0.01

# 0.0, 0.01, 0.02, ... , 254.99, 255.0 (총 25501개)
numerators = np.arange(start_num, max_num + step_num, step_num)

# 2. 이 분자 리스트 전체를 255로 나눔
EPSILON_LIST = numerators / 255.0

# --- 확인 ---
print(f"총 {len(EPSILON_LIST)}개의 Epsilon 값 생성됨")
print("\n리스트 시작 부분 (예시):")
print(EPSILON_LIST[:5])
# [ 0. (0/255)
#   0.00003921... (0.01/255)
#   0.00007843... (0.02/255)
#   0.00011764... (0.03/255)
#   0.00015686... (0.04/255) ]

print("\n리스트 끝 부분 (예시):")
print(EPSILON_LIST[-5:])
# [ 0.99984313... (254.96/255)
#   0.99988235... (254.97/255)
#   0.99992156... (254.98/255)
#   0.99996078... (254.99/255)
#   1.           (255/255) ]